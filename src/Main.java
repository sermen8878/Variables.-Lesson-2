public class Main {
    public static void main(String[] args) {
        int myIntNumber = 40000;
        byte myByteValue = 22;
        short myShortValue = 32000;
        long myLongValue = 123456789L;
        float myFloatValue = 3.75f;
        double myDoubleValue = 2.718281828459045;

        System.out.println("Значение переменной myIntNumber с типом int равно " + myIntNumber);
        System.out.println("Значение переменной myByteValue с типом byte равно " + myByteValue);
        System.out.println("Значение переменной myShortValue с типом short равно " + myShortValue);
        System.out.println("Значение переменной myLongValue с типом long равно " + myLongValue);
        System.out.println("Значение переменной myFloatValue с типом float равно " + myFloatValue);
        System.out.println("Значение переменной myDoubleValue с типом double равно " + myDoubleValue);
    }
}